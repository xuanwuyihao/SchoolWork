package bean;

public class Yzm {
	
	private int yid;
	private int bid;
	private int oid;
	private String yzm;
	private float yprice;
	private String ydate;
	public int getYid() {
		return yid;
	}
	public void setYid(int yid) {
		this.yid = yid;
	}
	public int getBid() {
		return bid;
	}
	public void setBid(int bid) {
		this.bid = bid;
	}
	public int getOid() {
		return oid;
	}
	public void setOid(int oid) {
		this.oid = oid;
	}
	public String getYzm() {
		return yzm;
	}
	public void setYzm(String yzm) {
		this.yzm = yzm;
	}
	public float getYprice() {
		return yprice;
	}
	public void setYprice(float yprice) {
		this.yprice = yprice;
	}
	public String getYdate() {
		return ydate;
	}
	public void setYdate(String ydate) {
		this.ydate = ydate;
	}
}
