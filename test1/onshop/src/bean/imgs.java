package bean;

public class imgs {
	private int lid;
	private int pid;
	private int bid;
	private int aid;
	private String lsrc;
	public int getLid() {
		return lid;
	}
	public void setLid(int lid) {
		this.lid = lid;
	}
	public int getPid() {
		return pid;
	}
	public void setPid(int pid) {
		this.pid = pid;
	}
	public int getBid() {
		return bid;
	}
	public void setBid(int bid) {
		this.bid = bid;
	}
	public int getAid() {
		return aid;
	}
	public void setAid(int aid) {
		this.aid = aid;
	}
	public String getLsrc() {
		return lsrc;
	}
	public void setLsrc(String lsrc) {
		this.lsrc = lsrc;
	}
}
