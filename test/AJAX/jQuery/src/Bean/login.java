package Bean;

public class login {

	private String id;
	private String pwd;
	public login(){}
	public login(String id,String pwd){}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getPwd() {
		return pwd;
	}
	public void setPwd(String pwd) {
		this.pwd = pwd;
	}
	
}
